// Helper function to get accommodation image and link by name
export const getAccommodationData = (name) => {
  // Search through all categories of hotels
  for (const category in accommodationImages.hotels) {
    if (accommodationImages.hotels[category][name]) {
      return accommodationImages.hotels[category][name];
    }
  }
  
  // Return default image and link if not found
  return {
    image: "https://images.unsplash.com/photo-1566073771259-6a8506099945",
    link: null
  };
};

